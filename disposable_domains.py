def is_disposable(email: str) -> bool:
    """
    Check if an email address uses a disposable domain.
    
    Args:
        email: Email address to check
        
    Returns:
        True if the email domain is in the disposable domains list
    """
    if '@' not in email:
        return False
    
    domain = email.split('@')[1].lower()
    return domain in DISPOSABLE_DOMAINS


def get_disposable_domains_list() -> list:
    """
    Get the list of disposable domains as a sorted list.
    
    Returns:
        Sorted list of disposable email domains
    """
    return sorted(DISPOSABLE_DOMAINS)
